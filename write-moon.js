L("    var sweepFlag2 = terminatorBulge > 0 ? 1 : 0;");
L("    litArc = 'M ' + mid + ' ' + topY + ' A ' + R + ' ' + R + ' 0 0 0 ' + mid + ' ' + botY + ' A ' + absT + ' ' + R + ' 0 0 ' + sweepFlag2 + ' ' + mid + ' ' + topY + ' Z';");
