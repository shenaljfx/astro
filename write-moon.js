L("      </Svg>");
L("    );");
L("  }");
L("");
L("  // New moon");
L("  if (illum < 0.003) {");
L("    return (");
L("      <Svg width={size} height={size} viewBox={'0 0 ' + size + ' ' + size}>");
L("        <Circle cx={mid} cy={mid} r={R} fill='#12101E' />");
