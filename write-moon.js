L("  return (");
L("    <Svg width={size} height={size} viewBox={'0 0 ' + size + ' ' + size}>");
L("      <Defs>");
L("        <ClipPath id={uid + 'disc'}><Circle cx={mid} cy={mid} r={R} /></ClipPath>");
L("        <ClipPath id={uid + 'lit'}><Path d={litArc} /></ClipPath>");
L("        <RadialGradient id={uid + 'rg'} cx='42%' cy='40%' r='58%'>");
L("          <Stop offset='0%' stopColor='#EAE4D8' />");
