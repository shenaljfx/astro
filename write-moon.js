// Seeded PRNG
L("var _rngState = 42;");
L("function _nextRand() {");
L("  _rngState = (_rngState * 16807) % 2147483647;");
L("  return (_rngState - 1) / 2147483646;");
L("}");
L("function _resetRng() { _rngState = 42; }");
L("");

// Procedural moon color texture
L("function buildLunarColorMap() {");
L("  if (cachedColorPixels) return cachedColorPixels;");
L("  _resetRng();");
L("  var w = TWIDTH, h = THEIGHT;");
L("  var px = new Uint8Array(w * h * 4);");
L("");
L("  // Pre-generate crater definitions");
L("  var numCraters = 200;");
L("  var craterList = [];");
L("  for (var ci = 0; ci < numCraters; ci++) {");
L("    var cRadius;");
L("    if (ci < 6) cRadius = 14 + _nextRand() * 22;");
L("    else if (ci < 25) cRadius = 6 + _nextRand() * 12;");
L("    else if (ci < 70) cRadius = 2 + _nextRand() * 5;");
L("    else cRadius = 0.8 + _nextRand() * 2.5;");
L("    craterList.push({");
L("      px: _nextRand() * w, py: _nextRand() * h,");
L("      rad: cRadius,");
L("      dep: 0.10 + _nextRand() * 0.25,");
L("      rim: 0.03 + _nextRand() * 0.09");
L("    });");
L("  }");
L("");
L("  // Dark plains (maria) regions");
L("  var darkPlains = [");
L("    {cx:0.38,cy:0.35,rx:0.13,ry:0.11,strength:0.17},");
L("    {cx:0.55,cy:0.40,rx:0.10,ry:0.09,strength:0.15},");
L("    {cx:0.60,cy:0.50,rx:0.11,ry:0.09,strength:0.14},");
L("    {cx:0.28,cy:0.52,rx:0.12,ry:0.18,strength:0.12},");
L("    {cx:0.48,cy:0.63,rx:0.10,ry:0.07,strength:0.11},");
L("    {cx:0.72,cy:0.42,rx:0.06,ry:0.05,strength:0.16},");
L("    {cx:0.66,cy:0.56,rx:0.08,ry:0.06,strength:0.12},");
L("    {cx:0.34,cy:0.71,rx:0.06,ry:0.05,strength:0.13},");
L("    {cx:0.43,cy:0.46,rx:0.07,ry:0.11,strength:0.11},");
L("    {cx:0.24,cy:0.38,rx:0.08,ry:0.06,strength:0.10},");
L("  ];");
L("");
L("  for (var row = 0; row < h; row++) {");
L("    for (var col = 0; col < w; col++) {");
L("      var offset = (row * w + col) * 4;");
L("      // Base highland brightness with subtle variation");
L("      var bright = 0.72 + _nextRand() * 0.04;");
L("      // Low frequency terrain undulation");
L("      var angX = col / w * 6.283;");
L("      var angY = row / h * 3.142;");
L("      bright += Math.sin(angX * 2.3 + angY * 1.7) * 0.028;");
L("      bright += Math.sin(angX * 5.1 - angY * 3.3) * 0.020;");
L("      bright += Math.sin(angX * 0.8 + angY * 4.2) * 0.016;");
L("      bright += Math.sin(angX * 8.5 + angY * 6.1) * 0.008;");
L("");
L("      // Apply dark plains");
L("      for (var pi = 0; pi < darkPlains.length; pi++) {");
L("        var dp = darkPlains[pi];");
L("        var fx = (col / w - dp.cx) / dp.rx;");
L("        var fy = (row / h - dp.cy) / dp.ry;");
L("        var fdist2 = fx * fx + fy * fy;");
L("        if (fdist2 < 1) {");
L("          var falloff = 1 - Math.sqrt(fdist2);");
L("          bright -= dp.strength * falloff * falloff;");
L("        }");
L("      }");
L("");
L("      // Apply craters");
L("      for (var ki = 0; ki < craterList.length; ki++) {");
L("        var cr = craterList[ki];");
L("        var dx = col - cr.px;");
L("        var dy = row - cr.py;");
L("        // Wrap horizontally for seamless tiling");
L("        if (dx > w / 2) dx -= w;");
L("        if (dx < -w / 2) dx += w;");
L("        var dist = Math.sqrt(dx * dx + dy * dy);");
L("        if (dist < cr.rad * 1.35) {");
L("          var normDist = dist / cr.rad;");
L("          if (normDist < 0.75) {");
L("            bright -= cr.dep * (1 - normDist / 0.75) * 0.5;");
L("          } else if (normDist < 1.0) {");
L("            bright -= cr.dep * 0.08 * (1 - (normDist - 0.75) / 0.25);");
L("          } else if (normDist < 1.25) {");
L("            bright += cr.rim * (1 - (normDist - 1.0) / 0.25);");
L("          }");
L("        }");
L("      }");
L("");
L("      // Micro-noise for grain");
L("      bright += (_nextRand() - 0.5) * 0.018;");
L("      bright = Math.max(0.04, Math.min(0.97, bright));");
L("");
L("      // Slightly warm-tinted gray (like real regolith)");
L("      px[offset]     = Math.floor(bright * 228);");
L("      px[offset + 1] = Math.floor(bright * 220);");
L("      px[offset + 2] = Math.floor(bright * 206);");
L("      px[offset + 3] = 255;");
L("    }");
L("  }");
L("  cachedColorPixels = px;");
L("  return px;");
L("}");
L("");

// Normal map generation
L("function buildLunarNormalMap() {");
L("  if (cachedNormPixels) return cachedNormPixels;");
L("  var colorPx = buildLunarColorMap();");
L("  var w = TWIDTH, h = THEIGHT;");
L("  var norms = new Uint8Array(w * h * 4);");
L("  var bumpStrength = 2.5;");
L("  for (var row = 0; row < h; row++) {");
L("    for (var col = 0; col < w; col++) {");
L("      var off = (row * w + col) * 4;");
L("      var heightL = colorPx[(row * w + ((col - 1 + w) % w)) * 4] / 255;");
L("      var heightR = colorPx[(row * w + ((col + 1) % w)) * 4] / 255;");
L("      var heightU = colorPx[(Math.max(row - 1, 0) * w + col) * 4] / 255;");
L("      var heightD = colorPx[(Math.min(row + 1, h - 1) * w + col) * 4] / 255;");
L("      var gradX = (heightL - heightR) * bumpStrength;");
L("      var gradY = (heightU - heightD) * bumpStrength;");
L("      var gradZ = 1.0;");
L("      var mag = Math.sqrt(gradX * gradX + gradY * gradY + gradZ * gradZ);");
L("      norms[off]     = Math.floor(((gradX / mag) * 0.5 + 0.5) * 255);");
L("      norms[off + 1] = Math.floor(((gradY / mag) * 0.5 + 0.5) * 255);");
L("      norms[off + 2] = Math.floor(((gradZ / mag) * 0.5 + 0.5) * 255);");
L("      norms[off + 3] = 255;");
L("    }");
L("  }");
L("  cachedNormPixels = norms;");
L("  return norms;");
L("}");
L("");

// computeLightAngle helper
L("function computeLightAngle(tithiNum) {");
L("  return ((tithiNum - 1) / 29) * Math.PI * 2;");
L("}");
L("");
L("function computeIllumFraction(tithiNum) {");
L("  return (1 - Math.cos(computeLightAngle(tithiNum))) / 2;");
L("}");
L("");

